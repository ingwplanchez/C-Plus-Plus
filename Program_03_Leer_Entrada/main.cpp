#include <iostream>

using namespace std;

int main()
{
    int number;
    cout << "Enter a decimal number: " << endl;
    cout << " " << endl;
    cin >> number;
    cout << " " << endl;
    cout << "value in octal = 0" << oct << number << endl;
    cout << " " << endl;
    cout << "value in hex = 0x" << hex << number << endl;
    cout << " " << endl;
    return 0;
}
